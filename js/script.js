  function calculatePercentage(arr1) {
    percentageChangePersonal = [];
    let firstIndex;
    let secondIndex;
    for (let i = 0; i < arr1.length - 1; i++) {
      firstIndex = arr1[i];
      secondIndex = arr1[i + 1];
      percentageChangePersonal.push(secondIndex / firstIndex);
    }

    let index,
      avg,
      above = 0,
      below = 0;

    for (let i = 0; i < personalCPI.length; i++) {
      index = personalCPI[i] / nationalCPI[i];

      if (index > 1) {
        above += 1;
      } else if (index < 1) {
        below += 1;
      }
    }

    if (above > below) {
      above = true;
      below = false;
    } else if ((above >= 5 && above < 7) || (below >= 5 && below < 7)) {
      avg = true;
      above = false;
      below = false;
    } else {
      below = true;
      above = false;
    }

    if (avg) {
      $(".output-text").html(
        "Inflation is affecting you close to the National Average <i class='fas fa-arrows-alt-h fa-2x'></i>"
      );
      $(".output-text").css("border", "2px solid blue");
      $(".fa-arrow-up").css("color", "blue");
    } else if (below) {
      $(".output-text").html(
        "Inflation is affecting you below the National Average <i class='fas fa-arrow-down fa-2x'></i>"
      );
      $(".output-text").css("border", "2px solid green");
      $(".fa-arrow-down").css("color", "green");
    } else if (above) {
      $(".output-text").html(
        "Inflation is affecting you above the National Average <i class='fas fa-arrow-up fa-2x'></i>"
      );
      $(".output-text").css("border", "2px solid red");
      $(".fa-arrow-up").css("color", "red");
    }
  }

